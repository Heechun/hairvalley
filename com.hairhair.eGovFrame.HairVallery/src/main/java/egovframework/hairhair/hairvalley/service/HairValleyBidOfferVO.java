package egovframework.hairhair.hairvalley.service;

public class HairValleyBidOfferVO {
	String company_name;
	String company_image;
	String company_id;
	int text_num;
	int offer_price;
	String add_offer;
	String reg_date;
	
	public String getCompany_name() {
		return company_name;
	}
	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}
	public String getCompany_image() {
		return company_image;
	}
	public void setCompany_image(String company_image) {
		this.company_image = company_image;
	}
	public String getCompany_id() {
		return company_id;
	}
	public void setCompany_id(String company_id) {
		this.company_id = company_id;
	}
	public int getText_num() {
		return text_num;
	}
	public void setText_num(int text_num) {
		this.text_num = text_num;
	}
	public int getOffer_price() {
		return offer_price;
	}
	public void setOffer_price(int offer_price) {
		this.offer_price = offer_price;
	}
	public String getAdd_offer() {
		return add_offer;
	}
	public void setAdd_offer(String add_offer) {
		this.add_offer = add_offer;
	}
	public String getReg_date() {
		return reg_date;
	}
	public void setReg_date(String reg_date) {
		this.reg_date = reg_date;
	}
	
		
}
